package com.leecottrell;

public class SimpleClass {
    public int addEm(int a, int b) {
        return a + b;
    }
    public int subEm(int a, int b) {
        return a - b;
    }
    public float divEm(int a, int b) {
        float ans = (float) a / b;
        // cast the division
        return ans;
    }
}